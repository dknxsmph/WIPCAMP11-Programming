public class Operator {
    public static void main(String[] args) {
        int x = 88;
        int y = 8;
        
        System.out.println("+ : "+ (x + y) );
        
        System.out.println("- : "+ (x - y) );
        
        System.out.println("* : "+ (x * y) );
        
        System.out.println("/ : "+ (y / x) );
        
        System.out.println("-y : "+ (x - -y) );
        
        System.out.println("% : "+ (y % x) );
        
    }
}
